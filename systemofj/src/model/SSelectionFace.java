package model;

public class SSelectionFace {


	//フィールド
	private int sId;
	private String sfCategory;
	private String sfName;
	private int sfScore;
	private int sfId;


	//getter・setter
	public int getsId() {
		return sId;
	}
	public void setsId(int sId) {
		this.sId = sId;
	}
	public String getSfCategory() {
		return sfCategory;
	}
	public void setSfCategory(String sfCategory) {
		this.sfCategory = sfCategory;
	}
	public String getSfName() {
		return sfName;
	}
	public void setSfName(String sfName) {
		this.sfName = sfName;
	}
	public int getSfScore() {
		return sfScore;
	}
	public void setSfScore(int sfScore) {
		this.sfScore = sfScore;
	}
	public int getSfId() {
		return sfId;
	}
	public void setSfId(int sfId) {
		this.sfId = sfId;
	}
}
